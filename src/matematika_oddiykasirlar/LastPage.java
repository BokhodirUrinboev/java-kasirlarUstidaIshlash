    private void YECHISHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_YECHISHActionPerformed
        

      int xNum=Integer.parseInt(x.getText());
      int yNum=Integer.parseInt(y.getText());
      int zNum=Integer.parseInt(z.getText());
      int tNum=Integer.parseInt(t.getText());
      Fraction fractionOne= new Fraction(xNum,yNum);
      Fraction fractionTwo= new Fraction(zNum,tNum);
      String operand=operator.getSelectedItem().toString();
         
         if(operand == "+")
         {
             javob.setText(fractionOne.addFraction(fractionTwo));
         }
         if(operand == "-")
         {
             javob.setText(fractionOne.subtractFraction(fractionTwo));
         }
         if(operand == "/")
         {
             javob.setText(fractionOne.divideByFraction(fractionTwo));
             
         }
          if(operand == "*")
          {
              javob.setText(fractionOne.multiplyByFraction(fractionTwo));
              
          }      
      
        
    }//GEN-LAST:event_YECHISHActionPerformed

